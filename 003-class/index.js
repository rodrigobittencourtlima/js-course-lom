// Javascript possui tipagem dinâmica
// Ponto e vírgula no JS é opcional
// Por padrão a função log quebra a linha a cada nova chamada
console.log('Rodrigo "B." de Lima');
console.log("Rodrigo 'B.' de Lima");
console.log(`'Rodrigo' "B." de Lima`);

// A diferença entre aspas simples e duplas ocorre apenas quando se deseja utilizar um desses dois caracteres na string
